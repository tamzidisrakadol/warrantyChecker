package com.example.warrantychecker.models;

public class Vendor {
    private int id;
    private String companyname,salesMan,city,area,phone,createDate;

    public Vendor(int id, String companyname, String salesMan, String city, String area, String phone, String createDate) {
        this.id = id;
        this.companyname = companyname;
        this.salesMan = salesMan;
        this.city = city;
        this.area = area;
        this.phone = phone;
        this.createDate = createDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCompanyname() {
        return companyname;
    }

    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    public String getSalesMan() {
        return salesMan;
    }

    public void setSalesMan(String salesMan) {
        this.salesMan = salesMan;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCreateDate() {
        return createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }
}
